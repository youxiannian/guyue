/**
  * @brief This function handles USART1 global interrupt / USART1 wake-up interrupt through EXTI line 25.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
	if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE))
	{
		__HAL_UART_CLEAR_FLAG(&huart1,UART_FLAG_IDLE);
		char txt[20];
		if(strcmp(buf_rec,"R37")==0){
			sprintf(txt,"R37:%.0f,%.0f,%.1f%%",sr37_test_num,sr37_num_right,sr37_pra);
			HAL_UART_Transmit(&huart1,(uint8_t *)txt,strlen(txt),0x1000);
		}
		else if(strcmp(buf_rec,"R38")==0){
			sprintf(txt,"R38:%0.f,%0.f,%.1f%%",sr38_test_num,sr38_num_right,sr38_pra);
			HAL_UART_Transmit(&huart1,(uint8_t *)txt,strlen(txt),0x1000);
		}
		len=0;
		memset(buf_rec,0,sizeof(buf_rec));
	}
	else
	{
		buf_rec[len]=USART1->RDR;
		len++;
		__HAL_UART_ENABLE_IT(&huart1,UART_IT_IDLE);
		HAL_UART_Receive_IT(&huart1,rec,1);
	}	
  /* USER CODE END USART1_IRQn 1 */
}

